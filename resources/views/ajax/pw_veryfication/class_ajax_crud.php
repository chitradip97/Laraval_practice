         function editTodo(id){
          console.log(id);
          $.ajax({
            'url':`{{url('/todo')}}/${id}`,
            'method':'GET',
            'data':{},
            'success': (data,status)=>{
                if(status =='success'){
                  console.log(data);
                  var jsonData = data;
                  $('#titleEditText').val(jsonData.title);
                  $('#descEditText').val(jsonData.description);
                  todo_id = jsonData.id;
